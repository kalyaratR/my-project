package day4;

public class FruitsFarmColors {
	public static void main(String[] args) {
		String apple = "Green";
		String banana = "Yellow";
		String orange = "pink";
		String kiwi = "blue";
		String pear = "Rad";
		
		System.out.println(apple); // green
		System.out.println(banana); // yellow
		System.out.println(orange); // pink
		System.out.println(kiwi); // blue
		System.out.println(pear); // red
		
		String allFruits = apple + "," + banana + "," + orange + "," + kiwi + "," + pear;
		System.out.println("Fruits: " + allFruits);
		
	}

}
